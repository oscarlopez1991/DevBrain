// Next-Themes Provider wrapper for App Router
//
// In Next.js App Router, all files in app/ are Server Components by default.
// Because the ThemeProvider from `next-themes` relies on React Context (which requires
// browser-only state/effects), we wrap it in a client component using "use client".
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

"use client"

import * as React from "react"
import { ThemeProvider as NextThemesProvider } from "next-themes"

export function ThemeProvider({
  children,
  ...props
}: React.ComponentProps<typeof NextThemesProvider>) {
  return <NextThemesProvider {...props}>{children}</NextThemesProvider>
}
